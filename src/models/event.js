const mongoose = require('mongoose');

const eventSchema = mongoose.Schema({
  title: {
    type: String,
    require: true,
  },
  location: {
    type: String,
    require: true,
  },
  date: {
    type: String,
    require: true,
  },
  time: {
    type: String,
    require: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  user: {
    type: mongoose.Schema.ObjectId,
    ref: 'User',
    required: true,
  },
});

mongoose.model('Event', eventSchema);
